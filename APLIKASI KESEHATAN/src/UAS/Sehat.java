    private void cHAPUSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cHAPUSActionPerformed
                                             
    if (!txusername.getText().equals("")) {
        int konfirmasi = JOptionPane.showConfirmDialog(this, 
            "Yakin ingin menghapus data ini?", 
            "Konfirmasi Hapus", 
            JOptionPane.YES_NO_OPTION);

        if (konfirmasi == JOptionPane.YES_OPTION) {
            try {
                destroyData();          // Jalankan perintah hapus di database
                lsDtbuku();             // Refresh tabel
                clearForm();            // Kosongkan form input
                fieldEnabled(false);    // Nonaktifkan field input
                tombol(false);          // Nonaktifkan tombol aksi
                cBARU.setEnabled(true);
                cTUTUP.setEnabled(true);
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus.");
            } catch (SQLException ex) {
                Logger.getLogger(Sehat.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat menghapus data.");
            }
        }
    } else {
        JOptionPane.showMessageDialog(this, "Silakan pilih data yang ingin dihapus terlebih dahulu.");
    }


    }//GEN-LAST:event_cHAPUSActionPerformed

