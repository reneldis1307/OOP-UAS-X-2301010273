    private void lsDtbuku() throws SQLException{
      Connection cnn = koneksi();
      dtm.getDataVector().removeAllElements();
      dtm.fireTableDataChanged();
      if( !cnn.isClosed() ){
          PreparedStatement ps = cnn.prepareStatement("SELECT * FROM sehat;");
          ResultSet rs = ps.executeQuery();
          while( rs.next() ){
              Object[] dta = new Object[4];
              dta[0] = rs.getString("username");
              dta[1] = rs.getString("password");
               dta[2] = rs.getString("notelepon");
              dta[3] = rs.getString("email");
             
              
            dtm.addRow(dta);
          
          }
          cnn.close();
    }
    }
    private void storeData() throws SQLException {
    Connection cnn = koneksi();
    if (!cnn.isClosed()) {
        PreparedStatement ps = cnn.prepareStatement("INSERT INTO sehat(username, password, notelepon, email) VALUES(?,?,?,?);");
        ps.setString(1, txusername.getText());
        ps.setString(2, txpassword.getText());
        ps.setString(3, txnotelepon.getText());
        ps.setString(4, txemail.getText());
        ps.executeUpdate();
        cnn.close();
    }
}

   private void updateData() throws SQLException {
    Connection cnn = koneksi();
    if (!cnn.isClosed()) {
        PreparedStatement ps = cnn.prepareStatement(
            "UPDATE sehat SET username=?, password=?, notelepon=? WHERE email=?;"
        );
        ps.setString(1, txusername.getText());
        ps.setString(2, txpassword.getText());
        ps.setString(3, txnotelepon.getText());
        ps.setString(4, txemail.getText()); // gunakan email sebagai kunci unik
        ps.executeUpdate();
        cnn.close();
    }
}

   private void destroyData() throws SQLException {
    Connection cnn = koneksi();
    if (!cnn.isClosed()) {
        String sql = "DELETE FROM sehat WHERE email = ?";
        PreparedStatement ps = cnn.prepareStatement(sql);
        ps.setString(1, txemail.getText()); // hapus berdasarkan email
        ps.executeUpdate();
        cnn.close();
    }
}

